    public void agregar(String nombre, double precio, int stock,String tipo, File ruta){
        ProductoVO vo = new ProductoVO();
        dao = new ProductoDAO();
        
        vo.setNombre(nombre);
        vo.setPrecio(precio);
        vo.setTipo(tipo);
        vo.setStock(stock);
        try{
            byte[] icono = new byte[(int) ruta.length()];
            InputStream input = new FileInputStream(ruta);
            input.read(icono);
            vo.setFoto(icono);
        }catch(Exception ex){
            vo.setFoto(null);
        }
        dao.Agregar_ProductoVO(vo);
        limpiar();
    }
    
    public void modificar(String nombre, double precio, int stock,String tipo,  File ruta, int id){
        ProductoVO vo = new ProductoVO();
        dao = new ProductoDAO();
        
        vo.setIdproducto(id);
        vo.setNombre(nombre);
        vo.setPrecio(precio);
        vo.setStock(stock);
        vo.setTipo(tipo);
        try{
            byte[] icono = new byte[(int) ruta.length()];
            InputStream input = new FileInputStream(ruta);
            input.read(icono);
            vo.setFoto(icono);
        }catch(Exception ex){
            vo.setFoto(null);
        }
        dao.Modificar_ProductoVO(vo);
    }
    
