    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        JFileChooser j = new JFileChooser();
        FileNameExtensionFilter fil = new FileNameExtensionFilter("JPG, PNG & GIF","jpg","png","gif");
        j.setFileFilter(fil);
        
        int s = j.showOpenDialog(this);
        if(s == JFileChooser.APPROVE_OPTION){
            String ruta = j.getSelectedFile().getAbsolutePath();
            txtruta.setText(ruta);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        
        if( txtn.getText().isEmpty() || txtp.getText().isEmpty() || txtm.getText().isEmpty() || txttipo.getText().isEmpty() || txtruta.getText().isEmpty() ){
         JOptionPane.showMessageDialog(this,"Los campos no pueden ir vacios");    
        }else {
           
             
              String nombre = txtn.getText();
        double precio = Double.parseDouble(txtp.getText());
        int stock = Integer.parseInt(txtm.getText());
        String tipo = txttipo.getText();
        File ruta = new File(txtruta.getText());
        
        this.agregar(nombre, precio, stock,tipo, ruta);
        t.visualizar_ProductoVO(tabla);
         JOptionPane.showMessageDialog(this,"producto agregado con exito");   
        
         }
       
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        int clic = tabla.rowAtPoint(evt.getPoint());
        
        codigo = (int)tabla.getValueAt(clic, 0);
        String nombre = ""+tabla.getValueAt(clic, 1);
        double precio = (double)tabla.getValueAt(clic, 2);
        int stock = (int)tabla.getValueAt(clic, 3);
        String tipo = ""+tabla.getValueAt(clic, 4);
        
        
        
        txtc.setText(String.valueOf(codigo));
        txtn.setText(nombre);
        txtp.setText(String.valueOf(precio));
        txtm.setText(String.valueOf(stock));
        txttipo.setText(tipo);
        txtruta.setText("");
        
    }//GEN-LAST:event_tablaMouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int id = Integer.parseInt(txtc.getText());
        String nombre = txtn.getText();
        double precio = Double.parseDouble(txtp.getText());
        int stock = Integer.parseInt(txtm.getText());
        String tipo = txttipo.getText();
        File ruta = new File(txtruta.getText());
        
        if(ruta!=null){
            this.modificar(nombre, precio, stock,tipo,ruta, id);
            t.visualizar_ProductoVO(tabla);
            limpiar();
        }else{
            this.modificar2(nombre, precio, stock,tipo,id);
            t.visualizar_ProductoVO(tabla);
            limpiar();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

